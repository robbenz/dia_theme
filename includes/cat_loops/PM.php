<strong>Our preventative maintenance process includes the following:</strong>
<ul>
 	<li>Identification of equipment or systems that may malfunction.</li>
 	<li>Establishment of schedules and procedures for inspection.</li>
 	<li>Periodic testing of equipment for structural soundness.</li>
 	<li>Prompt repair or replacement of defective equipment found during inspection and testing.</li>
 	<li>Inventory of spare parts for equipment that needs frequent repairs.</li>
 	<li>Commitment to ensure that records are complete and detailed, and that they record test results and follow-up actions.</li>
</ul>

<p>DiaMedical USA maintains a large network of full-time outside biomedical technicians. These individuals are certified technicians that receive extensive training and continuing education for the product lines they support. Our service network allows us to provide ongoing equipment management in the field.</p>

<p>Each piece of equipment is backed by a one year warranty. Our on-site maintenance team provides your facility with 24-hour availability, access to discounted parts and expedited parts replacement. The result is well-maintained, quality equipment with minimal downtime all while saving money.</p>


